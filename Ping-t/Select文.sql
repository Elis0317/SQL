問題ID： 7502

列別名はオブジェクトのネーミング規則に従って命名されますが、大文字と小文字を区別したり、
ネーミング規則に反する列別名(スペースを使用するなど)を使用する場合は、列別名を二重引用符("")で囲まなければなりません。

ネーミング規則は以下のとおりです。

　・30バイト以下であること
　・SQLの予約語(SELECT, DATEなど)は使用不可
　・英数字、特殊文字(#,$,_)が使用可
　・日本語環境では、漢字、ひらがな、カタカナも使用可
　・数字、特殊文字(#,$,_)は名前の先頭に使用不可
　・スペースは使用不可

e.g.
SELECT employee_id "社員No", employee_name 社員名, 
salary * 12 "給与(年収)" FROM employees;

--社員Noは大文字と小文字の区別をつけるため。
--給与年収はネーミング規則に反する列別名(()が使われている)ため。

また、別名に関する注意点として、「算術式の中で列別名を使うことはできない」というルールがあります。
SELECT salary * 12 AS "Sal", 
Sal + commission AS "Total"	--エラー（Salを使うことはできない)
FROM employees;



