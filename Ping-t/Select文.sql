SQL文の実行結果に表示される列見出しを変更したい場合は、SELECT句に列別名を指定します。列別名は算術式にも指定することができます。
列別名は項目名と列別名をスペースで区切るか、明示的にASキーワードで指定します。

列別名はオブジェクトのネーミング規則に従って命名されますが、大文字と小文字を区別したり、
ネーミング規則に反する列別名(スペースを使用するなど)を使用する場合は、列別名を二重引用符("")で囲まなければなりません。
列別名は一重引用符('')ではなく二重引用符("")で囲みます。このSQL文はエラーとなります。

なお、
SELECT employee_name "Employee Name", salary AS Salary FROM employees;
はエラーとはなりませんが、列別名 Salary が二重引用符で囲まれていないので、列見出しは大文字でSALARYとなります。



