SQL文中で指定する固定の文字列などをリテラルといいます。
リテラルには、
　・文字リテラル
　・数値リテラル
　・日付リテラル
がありますが、このうち、文字リテラルと日付リテラルを指定する場合は、リテラルを区切り文字（デリミタと言います）である一重引用符('')で囲まなければなりません。
リテラルは連結演算子||と組み合わせることで、列の値や算術式の結果と連結した文字列を生成できます。

ですが、文字リテラルの一部に一重引用符('')を使用したい場合もあります。
