　　・任意の文字(大文字と小文字は区別されます)
　　・[],<>,(),{}の組合せ

　代替引用符q演算子は以下ように使用します。
　　q'引用符デリミタ ... 引用符デリミタ'

　代替の引用符デリミタを用いることで、...の部分では、一重引用符('')を文字として扱えます。なお、q演算子のqは大文字のQと記述することもできます。

