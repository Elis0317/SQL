ROUND関数は、引数で指定された日付値を丸めて返します。
使用法は以下の通りです。

　ROUND(日付値[, '書式'])

どの単位で丸めるかは書式で指定します。指定できる主な書式は次の通りです。
書式が省略された場合は'DD'が指定されたものとして処理されます。

YEAR:
	日付が6/30以前=>当年の1月1日午前0時を返す
	日付が7/1以降=>翌年の1月1日午前0時を返す
MONTH:
	日付が15日以前=>当月の1日午前0時を返す
	日付が16日以降=>翌月の1日午前0時を返す
DD
	日付が正午より前=>当日の午前0時を返す
	日付が正午より後=>翌日の午前0時を返す

主な日付関数には次のものがあります。
SYSDATE	現在の日時を取得する
MONTHS_BETWEEN	2つの日付間の月数を取得する
ADD_MONTHS	nか月後の日付を取得する
NEXT_DAY	指定した曜日になる最初の日付を取得する
LAST_DAY	指定した月の最終日を取得する
ROUND	指定した日付を丸める
TRUNC	指定した日付を切り捨てる



