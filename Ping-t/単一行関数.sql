日付値に対し、加算や減算の演算を行えます。日付値に対して実行できる演算は次のとおりです。

　[日付値 + 数値, 日付値 - 数値]
　数値を日数として日付値に加算/減算します。演算結果は加算/減算後の日付値となります。


　[日付値 + 数値/24, 日付値 - 数値/24]
　数値を時間数として日付値に加算/減算します。演算結果は加算/減算後の日付値となります。

