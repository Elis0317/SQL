TRIM関数は、単一行関数のうちの文字関数に分類されます。
引数で指定された文字列の前後にある削除文字を取り除いた文字列を返します。
使用法は以下の通りです。

　TRIM([LEADING | TRAILING | BOTH] [削除文字] FROM 文字列)
または
　TRIM(文字列)

e.g. SELECT TRIM(BOTH '#' FROM '#Oracle Web問題集#')

LEADING,TRAILING,BOTHは削除する位置の指定です。省略した場合は前後の削除文字が取り除かれます。

・LEADING：先頭の削除文字を取り除く
・TRAILING：末尾の削除文字を取り除く
・BOTH：前後の削除文字を取り除く（デフォルト）

削除文字には任意の1文字を指定できますが、文字列は指定できません。省略された場合は半角スペースが削除文字として取り除かれます。
「FROM」の省略は、削除位置と削除文字の両方が省略された場合にのみ可能です。



